let valor1 = 10
let valor2 = 20

console.log(`Valor 1: ${valor1}; Valor 2: ${valor2}`)

//Valor diferente
if (valor1 != valor2){
    console.log("valor1 é diferente de valor2")
}else{
    console.log("valor1 é igual a valor2")
}

//Valor iguais
if (valor1 == valor2){
    console.log("valor1 é igual a valor2")
}else{
    console.log("valor1 é diferente de valor2")
}

//Valor maior
if (valor1 >= valor2){
    console.log("valor1 é maior ou igual que valor2")
}else if (valor1 <= valor2){
    console.log("valor1 é menor ou igual que valor2")
}